namespace mhxy.Common.Model {

    /// <summary>
    /// 基本属性
    /// </summary>
    public class Attribute {

        /// <summary>
        /// 体质属性
        /// </summary>
        public int Physique { get; set; }

        /// <summary>
        /// 力量属性
        /// </summary>
        public int Strength { get; set; }

        /// <summary>
        /// 法力属性
        /// </summary>
